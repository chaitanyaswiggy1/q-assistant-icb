def stream_portkey(api_key, model, messages):
    resp = _http.post(
        "https://api.portkey.ai/v1/chat/completions",
        headers={"x-portkey-api-key": api_key, "Content-Type": "application/json"},
        json={"model": model, "messages": messages, "temperature": 0.7, "stream": True},
        stream=True, timeout=60,
    )
    resp.raise_for_status()
    for raw in resp.iter_lines():
        if not raw: continue
        line = raw.decode("utf-8") if isinstance(raw, bytes) else raw
        if not line.startswith("data: "): continue
        payload = line[6:].strip()
        if payload == "[DONE]": break
        try:
            chunk = json.loads(payload)
            delta = (chunk["choices"][0]["delta"].get("content") or "") if chunk.get("choices") else ""
            if delta: yield delta
        except (json.JSONDecodeError, KeyError, IndexError):
            pass

